namespace Rant.Vocabulary.Lingusitics.German
{
    /// <summary>
    /// Lists the grammatical cases of the German language.
    /// <para></para>
    /// Aufzählt die Fälle der deutschen Sprache.
    /// </summary>
    internal enum GrammaticalCase
    {
        /// <summary>
        /// The nominative case (der Nominativ).
        /// </summary>
        Nominative,
        /// <summary>
        /// The accusative case (der Akkusativ).
        /// </summary>
        Accusative,
        /// <summary>
        /// The dative case (der Dativ).
        /// </summary>
        Dative,
        /// <summary>
        /// The genitive case (der Genitiv).
        /// </summary>
        Genitive
    }
}